"""Базовый класс доменных ошибок и перевод исключений в текст для человека.

Контракт: текст ``EngineError`` и её подклассов написан для пользователя
(по-русски, с подсказкой, что делать) — интерфейс показывает его как есть.
Любое другое исключение — техническая неожиданность: наружу уходит короткая
сводка (:func:`user_message`), полные детали пишутся в лог.
"""

from __future__ import annotations

#: Предел длины технической части в тексте «внутренней ошибки».
_DETAIL_CHARS = 160


class EngineError(Exception):
	"""Ошибка движка с понятным человеку текстом (можно показывать как есть).

	Все доменные ошибки сервисов и транспортов наследуются от неё —
	это признак «текст безопасен для интерфейса». Новая доменная ошибка
	обязана наследовать этот класс, иначе мост покажет её как внутреннюю.
	"""


def user_message(exc: BaseException) -> str:
	"""Текст исключения для показа человеку.

	Доменные ошибки (:class:`EngineError`) и сетевые (``ConnectionError``
	с нашим текстом на границах транспортов) показываются как есть.
	Остальное сворачивается в «внутреннюю ошибку»: имя класса и первая
	строка (у ошибок СУБД и библиотек текст бывает многоэкранным дампом,
	человеку он не помогает — полная версия остаётся в логе).
	"""
	if isinstance(exc, EngineError | ConnectionError):
		return str(exc)
	first_line = str(exc).strip().splitlines()[0] if str(exc).strip() else ""
	if len(first_line) > _DETAIL_CHARS:
		first_line = f"{first_line[:_DETAIL_CHARS]}…"
	detail = f": {first_line}" if first_line else ""
	return (
		f"Внутренняя ошибка ({type(exc).__name__}{detail}) — "
		"подробности в логе приложения."
	)
