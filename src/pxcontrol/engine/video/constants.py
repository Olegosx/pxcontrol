"""Общие константы и геометрия целевого кадра (порт из makeVideo).

Размер итогового кадра считается в Python (:func:`fitted_size`), а не
фильтром ffmpeg с force_original_aspect_ratio: основное видео и кадр
заставки должны получить ровно одинаковые размеры — это требование
фильтра склейки xfade. Два независимых вписывания в ffmpeg могли давать
расхождение в размерах и валить обработку.
"""

# Рамка вписывания: FullHD.
TARGET_WIDTH = 1920
TARGET_HEIGHT = 1080

# Формат пикселей для совместимости с большинством плееров и H.264.
TARGET_PIX_FMT = "yuv420p"

# Матрица преобразования RGB→YUV для вотермарка: кадр всегда HD
# (вписывание в 1920×1080), стандарт HD — bt709. Матрица по умолчанию
# в ffmpeg — bt601 (SD): из-за несовпадения с разметкой файла плеер
# сдвигал оттенки вотермарка.
TARGET_COLOR_MATRIX = "bt709"


def fitted_size(width: int, height: int) -> tuple[int, int]:
	"""Размер кадра, вписанного в рамку FullHD с сохранением пропорций.

	Стороны округляются до чётных (требование кодека H.264). Кадр меньше
	рамки растягивается до неё — как вело себя вписывание в makeVideo.

	Args:
		width: ширина исходного кадра в пикселях.
		height: высота исходного кадра в пикселях.

	Returns:
		Пара (ширина, высота) итогового кадра.
	"""
	ratio = min(TARGET_WIDTH / width, TARGET_HEIGHT / height)
	return round(width * ratio / 2) * 2, round(height * ratio / 2) * 2
